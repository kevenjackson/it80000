<h1>1、颍川陈氏先祖祖宗祠堂-河南颍川堂的由来</h1>

<p align="left">
河南颍川堂，是我们的陈氏先祖在近千年的定居颍川过程中建立的祖宗祠堂，是中国百家姓中的著名堂号之一。能籍由一个姓氏而产生一个姓氏堂号，一般是姓氏发祥地，并且是该地区的望族。颍水出于河南省西境颍谷，向东南流，入淮河；另一支从阳城县阳乾山，颍水所出，东入淮河。过三郡，行千五百里，古时颍水称颍川。<br>
以郡望颍川作为堂号，表示该姓氏的先祖是当时颍川地域的望族。<br>
从字面含义就可以看出贴有颍川堂的家、村、宗族、祠堂等的人士就是从颍川迁徙而来的某姓氏后裔。<br>
赖氏、陈氏、钟氏、邬氏、甘氏等姓氏有不少分支以颍川堂为堂号。<br>
颍川是一个郡名，秦王政17年（公元前230年）置，以颍水得名。古老的颍水，也称作颍河，发源于中岳嵩山，迤逦东下，流经河南登封、禹州、许昌、临颍、周口、颍上、阜阳汇入淮河，为淮河第一大支流。颍川在历史上一直是大郡，自设立以后一直是京师之外人口最多，最为繁华的地方，治所在今河南省禹州市。颍川郡有深厚的文化底蕴，是中华民族的发祥地，黄帝生于此，夏禹也建都于颍川。颍川因此成为中国众多姓氏的发祥地。历史上颍川人才辈出，数不胜数。<br>
虽然一些姓氏有着共同的堂号，但其对应的门联是有区别的。<br><br>


赖氏颍川堂，堂号颍川、松阳、西川、南康、河南、积善。<br>
其祠堂门联为：秘书世德；上策家声。<br>
其祠堂堂联为：秘书世德；上策家声。<br><br>

陈氏颍川堂，堂号颍川、汝南、敦睦。<br>
其祠堂门联为：柳溪世德，循铎家声。<br>
其祠堂堂联为（各地不同）：<br>
颍川世泽，世胃家声。<br>
东山世德；颍水家声。<br>
汝南世德；御史家声。<br>
柳溪源远；循铎声宏。<br><br>

钟氏颍川堂,堂号颍川。<br>
其祠堂门联为：金陵世德，越国荣封。<br>
其祠堂堂联为(各地不同)：<br>
颍川世泽；太傅家声。<br>
荥阳世泽，花县家声。<br>
晋阳世泽；长史家声。<br><br>

甘氏颍川堂，堂号颍川。<br>
其祠堂堂联为：望出渤海；源自甘国。<br><br>

颍川非颖川<br>
在春节前夕写对联的时候，由于疏忽，经常会有不少人将“颍川堂”的“颍”写成“聪颖”的“颖”，从而误导了人们。堂号是一个家族源流世系，区分族属、支派的标记，是寻根意识与祖先崇拜的体现。堂号比姓名更为重要，实在不应写错。颍川堂与颖川堂，俨然真假美猴王。颍川堂得名于颍水，所以，有“水”的“颍川堂”才是真正的“美猴王”。</p>

