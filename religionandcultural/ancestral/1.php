<h1>2、甫背陈家宗祠——甫背思敬堂的由来</h1>
<p align="left">
甫背思敬堂始建于公元1730~1740年清乾隆年间，属颍川堂分支!<br>
中华陈氏第95世之陈桂位--字思泉，是甫背的开山基祖，尊桂位公，或思泉公。<br>
自思泉公以下，其子嗣一直单传直至第99世陈本兴（尊本兴公）。方得陈安集+陈安完+陈安美+陈安足四公（中华陈氏第100世），自此，陈氏族孙方才人丁兴旺并最终在甫背站稳脚跟，甫背陈氏一个全新的格局，方才到来。后在陈赖仁（本兴）的带领下，集全族之力终于在约1730~1740年间兴建起甫背思敬堂，喻意甫背陈家子子孙孙，永世不忘先祖。同时，原来居住在甫背的朱姓，曾姓，古姓先后搬离甫背。<br>
思言敬事 岀处：《大戴礼记》：<br>
原文：君子虑胜气，思而后动，论而后行，行必思言之，言之必思复之，思复之必思无悔言，亦可谓慎矣。
人信其言，从之以行，人信其行，从之 ...
取地之财而节用之，抚教万民而利诲之，历日月而迎送之，明鬼神而敬事之。
其色郁郁，其德嶷嶷，其动也时，其服也士。
现代汉语释义为：饮水思源不忘本，爱敬天地亲君师！<br>
