<h1>公元1730~1740年间甫背陈家宗祠的第一次兴建</h1>
<p align="left">
约在公元1730~1740清乾隆年间，在中华第143世-即陈姓第99世之陈赖仁(字本兴)的带领下，集当时的全族之力，于甫背村的禁山神龙明珠墩前的风水宝地上，兴建起了甫背的第一座陈氏祖宗祠堂-思敬堂，以纪念陈氏子孙历经千万年无数次九死一生之万千劫难后终仍传承不息之精神和命运，及遥记河南颍川先祖之传承不息之血脉亲恩，取名思敬，亦意为感念历代列祖列宗在天之灵于瞑瞑中之万千庇佑，并遥寄无限衰思于历代列祖列宗在天之英魂。</p>

<h1>公元2012~2015年间甫背陈家宗祠的第一次重建</h1>
<p align="left">
陈氏祖宗祠堂自公元1730~1740年间第一次兴建后，历经270余载的风风雨雨，庇佑了一代又一代的陈氏子孙，永昌繁茂、子孙无数，至公元2012年左右，原土木结构之构造已届不惑，正好恰逢时中华人民共和国历经30多年的改革开放后，国富民强，我甫背家族各家各户亦通过分赴改革开放的前沿地——广东打工，而逐渐积聚起了一定的财源，故而于公元2012年，在中华总第151世-即陈氏第107世之陈开有的倡议下，由时甫背家族理事会会长陈浪清带头担纲，在甫甫家族理事会的带领下，集甫背全族之人力、财力、和物力（含外嫁的女儿及其夫家之财力），并获得本族陈开有（中华总第151世-即陈姓第107世）、陈伟（中华总第151世-即陈姓第107世）、陈同华（中华总第151世-即陈姓第107世）、陈民生（中华总第152世-即陈姓第109世）、陈天发（中华总第151世-即陈姓第107世）、陈同昌（中华总第151世-即陈姓第107世）等多位陈氏子孙乡绅和善士的慷慨解囊相助，终于于公元2013年完成了甫背陈家宗祠之辅楼——风水门楼的第一次重建，并于公元2015年再次完成了甫背陈家宗祠之主祠——甫背思敬堂的第一次重建。<br><br>
为让子孙后代永记此一重大盛事，特于甫背陈家宗祠之主祠——甫背思敬堂的正大门外，刻一巨型石碑以记之。
