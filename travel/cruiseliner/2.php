甫背大开发八大项目工程介绍<br>
1、甫背小公园+甫背小西湖+甫背小水库：<br>
于镇中心寸土寸金的黄金地段，打造出一片闹中取静的幽幽净土，风水+人文+自然景观的和谐统一，处处诠释着千百年客家文明古朴的文化气息。<br>
得益于杭州西湖人文自然和谐共处的设计理念，并遵循客家文明千百年传承不息的风水文化精髓及现代生态科学的设计理念，一幅美丽清新的甫背小西湖画卷即将呈现于世人的眼前。<br>
两处荷花一样红，此亦西湖，彼亦西湖，常把西湖比西子；<br>
一般胜境无二致，不是天堂，胜似天堂，疑是明珠落玉盘。<br><br>

2、甫背乡村度假别墅群：<br>
远离喧嚣污浊的都市尘缘，挥别寒冷刺骨的北方长夜、逃离赤如烈焰的南方火炉，这里四季如春、温暖花开、旷野一碧、流水潺潺......<br>
千百年客家文明古朴醇厚的浓浓乡情，陶渊明釆菊东篱的悠然淡定，定会让你忘却都市的烦恼，流连乡村的宁静，千里依稀田园梦，恨不归隐为乡人。<br><br>

3、100%原汁原味的甫背原生态休闲农庄：<br>
潺潺的流水，清沏见底的溪流，100%原汁原味的乡村小水库，空旷静谥的山谷清幽，婉转动听的百鸟争鸣、犹如置身世外桃源般美丽的甫背原生态休闲农庄，正徐徐的展开，正是您休闲、娱乐、旅游的绝佳去处。<br><br>

4、动植物和谐共处的甫背原生态百果园+甫背蔬菜种植示范基地：<br>
山中百花竞放，山下猪肥羊瘦，千姿百媚争相艳，香飘万里百果园。<br><br>

5、靖石未来的高级商务区：<br>
借助宁（都）定（南）高速的巨大发展优势，于靖石高速出口处，倾全靖石之力打造出未来靖石的高级商务区，未来的靖石高级商务区，酒店、宾馆、高级写字楼、商务中心、大商业超市、商业步行街、大型集散型综合批发市场（如蔬菜水果批发市场）将迅速崛起。<br>
高楼林立，大厦迎风，一个崭新的时代，正昂首阔步，迎面而来......</p>

